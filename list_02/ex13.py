#Faça um Programa que leia um número e exiba o dia correspondente da semana. (1-Domingo, 2- Segunda, etc.), se digitar outro valor deve aparecer valor inválido
dia=int(input('Digite um numero de 1 a 6\n'))
if dia == 1:
    print('Domingo')
if dia == 2:
    print('Segunda-Feira')
if dia == 3:
    print('Terça-Feira')
if dia == 4:
    print('Quarta-Feira')
if dia == 5:
    print('Quinta-Feira')
if dia == 6:
    print('Sexta-Feira')
